// ShoppingManager interface
public interface ShoppingManager {

    // Method to display the menu options for the shopping manager
    void menueDisplay();

    // Method to add a product to the shopping manager
    void addProduct();

    // Method to remove a product from the shopping manager
    void removeProduct();

    // Method to print the list of products managed by the shopping manager
    void printProducts();

    // Method to calculate the total cost of the products in the shopping manager
    void calculateTotalCost();
}
